#!/bin/bash

echo -ne '\033c'

term_width=$(tput cols)

if [ "$term_width" -gt 71 ]; then

    term_lines=$(tput lines)
    
    if [ "$term_lines" -gt 26 ]; then

        left_padding=$(((term_width / 2) - 36))
        top_padding=$(((term_lines / 2) - 12))

        printf '\n%.0s' $(seq 1 $top_padding)

        printf  "%${left_padding}s%s\e[1;93m              ..oo\$00ooo..                    ..ooo00\$oo..\n"
        printf  "%${left_padding}s%s           .o\$\$\$\$\$\$\$\$\$'                          '\$\$\$\$\$\$\$\$\$o.\n"
        printf  "%${left_padding}s%s        .o\$\$\$\$\$\$\$\$\$\"             .   .              \"\$\$\$\$\$\$\$\$\$o.\n"
        printf  "%${left_padding}s%s      .o\$\$\$\$\$\$\$\$\$\$~             /\$   \$\              ~\$\$\$\$\$\$\$\$\$\$o.\n"
        printf  "%${left_padding}s%s    .{\$\$\$\$\$\$\$\$\$\$\$.              \$\___/\$               .\$\$\$\$\$\$\$\$\$\$\$}.\n"
        printf  "%${left_padding}s%s   o\$\$\$\$\$\$\$\$\$\$\$\$8              .\$\$\$\$\$\$\$.               8\$\$\$\$\$\$\$\$\$\$\$\$o\n"
        printf  "%${left_padding}s%s  \$\$\$\$\$\$\$\$\$\$\$\$\$\$\$              \$\$\$\$\$\$\$\$\$               \$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\n"
        printf  "%${left_padding}s%s\e[0;93m o\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$.             o\$\$\$\$\$\$\$o              .\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$o\n"
        printf  "%${left_padding}s%s \$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$.           o{\$\$\$\$\$\$\$}o            .\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\n"
        printf  "%${left_padding}s%s^\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$.         J\$\$\$\$\$\$\$\$\$\$\$L          .\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$^\n"
        printf  "%${left_padding}s%s!\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$oo..oo\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$oo..oo\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$!\n"
        printf  "%${left_padding}s%s{\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$}\n"
        printf  "%${left_padding}s%s\e[1;33m6\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$?\n"
        printf  "%${left_padding}s%s'\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$'\n"
        printf  "%${left_padding}s%s o\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$o\n"
        printf  "%${left_padding}s%s  \$\$\$\$\$\$\$\$\$\$\$\$\$\$;'~\`^Y\$\$\$7^''o\$\$\$\$\$\$\$\$\$\$\$o''^Y\$\$\$7^\`~';\$\$\$\$\$\$\$\$\$\$\$\$\$\$\$\n"
        printf  "%${left_padding}s%s  '\$\$\$\$\$\$\$\$\$\$\$'       \`\$'    \`'\$\$\$\$\$\$\$\$\$'     \`\$'       '\$\$\$\$\$\$\$\$\$\$\$\$'\n"
        printf  "%${left_padding}s%s   !\$\$\$\$\$\$\$\$\$7         !       '\$\$\$\$\$\$\$'       !         V\$\$\$\$\$\$\$\$\$!\n"
        printf  "%${left_padding}s%s\e[0;33m    ^o\$\$\$\$\$\$!                   '\$\$\$\$\$'                   !\$\$\$\$\$\$o^\n"
        printf  "%${left_padding}s%s      ^\$\$\$\$\$\"                    \$\$\$\$\$                    \"\$\$\$\$\$^\n"
        printf  "%${left_padding}s%s        'o\$\$\$\`                   ^\$\$\$'                   '\$\$\$o'\n"
        printf  "%${left_padding}s%s          ~\$\$\$.                   \$\$\$.                  .\$\$\$~\n"
        printf  "%${left_padding}s%s            '\$;.                  \`\$'                  .;\$'\n"
        printf  "%${left_padding}s%s               '.                  !                  .\`\n"
        echo
    fi
fi